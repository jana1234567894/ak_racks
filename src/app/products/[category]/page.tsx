import { Metadata } from 'next';
import { notFound } from 'next/navigation';
import Link from 'next/link';
import { productCategories, getCategoryBySlug, getAllCategorySlugs, availableColors } from '@/lib/products';
import { siteConfig, generateCategoryMeta } from '@/lib/seo';
import { ProductCard } from '@/components/products';

interface PageProps {
    params: Promise<{ category: string }>;
}

export async function generateStaticParams() {
    return getAllCategorySlugs().map((slug) => ({
        category: slug,
    }));
}

export async function generateMetadata({ params }: PageProps): Promise<Metadata> {
    const { category } = await params;
    const categoryData = getCategoryBySlug(category);

    if (!categoryData) {
        return { title: 'Category Not Found' };
    }

    const meta = generateCategoryMeta(categoryData.name, categoryData.description);

    return {
        title: meta.title,
        description: meta.description,
        keywords: meta.keywords,
    };
}

